int		ft_wordcount(char const *s, char c)
{
	int		i;
	int		word;

	word = 0;
	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			while (s[i] == c)
			{
				i++;
			}
			if (s[i] != c && s[i] != '\0')
			{
				word++;
				i++;
			}
			while (s[i] != c && s[i] != '\0')
				i++;
		}
	}
	return (word);
}
