#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	n1;
	char			sgn;

	sgn = 1;
	n1 = n;
	if (n < 0)
	{
		ft_putchar('-');
		n1 = -n;
	}
	if (n1 / 10 != 0)
		ft_putnbr(n1 / 10);
	ft_putchar((n1 % 10) + '0');
}
